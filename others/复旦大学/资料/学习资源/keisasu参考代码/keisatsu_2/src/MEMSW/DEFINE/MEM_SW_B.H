#define	MEM_TX_BPS264	10
#define	MEM_TX_BPS288	11
#define	MEM_TX_BPS312	12
#define	MEM_TX_BPS336	13
/*--------------------------------------------------------------------------------
B:0-4 Ｖ２９でＥＰ　ＴＯＮＥ付きで送信 ０：しない　１：する			*/
#define	V29_EPTONE_AVAILABLE	0x10
/*--------------------------------------------------------------------------------
B:0-5 着信後１回目のＤＩＳを無視 ０：しない　１：する				*/
#define	OVERRIDE_1ST_DIS	0x20
/*--------------------------------------------------------------------------------
B:0-6 フォールバックパターン 2.4k 4.8k 7.2k 9.6k 12.0k 14.4k
	０：2回  2回  2回  2回   2回   2回
	１：4回  1回  1回  1回   1回   1回					*/
#define	FALLBACK_PATTERN	0x40
/*--------------------------------------------------------------------------------
/*B:0-7発呼後BUSY TONEを検出		０：しない　１：する			*/
#define	MEM_BUSYTONE_DETECT	0x80
/*------------------------------------------------------------------------------*/

/********************************************************************************/
/*スイッチＢ：１	内容	初期値						*/
/********************************************************************************/
#define	MEMORY_SW_B1	11
