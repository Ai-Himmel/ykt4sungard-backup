/********************************************************************************/
/*スイッチＢ：３	内容	初期値						*/
/********************************************************************************/
#define	MEMORY_SW_B3	13
/*--------------------------------------------------------------------------------
B:3-0 14.4K(V17)短縮ハンドシェイク無効/有効の獲得				*/
#define	SPEED_HANDSHAKE_144			0x01
/*--------------------------------------------------------------------------------
B:3-1 9.6K(V29)短縮ハンドシェイク無効/有効の獲得				*/
#define	SPEED_HANDSHAKE_96			0x02
/*--------------------------------------------------------------------------------
B:3-2 Ｖ８短縮ハンドシェイク無効/有効の獲得					*/
#define	SPEED_HANDSHAKE_V8			0x04
/*--------------------------------------------------------------------------------
B:3-3 RTN,PINを受信しても自動ページ再送無効/有効の獲得				*/
#define	RTN_AUTO_RETRANSMIT_DISABLE	0x08
/*--------------------------------------------------------------------------------
B:3-4 ＥＣＭ ＯＮ／ＯＦＦ														*/
#define ECM_TX_OFF					0x10
/*--------------------------------------------------------------------------------
B:3-5 端末識別信号送出禁止 ０：送出する(禁止しない)、１：送出しない(禁止する）1999/02/04 Y.Murata */
#define NO_TERMINAL_INFORMATION_TX		0x20
/*--------------------------------------------------------------------------------
B:3-6 スーパーＧ３送信有効／無効の獲得											 */
/* このビットは実際の送信時にスーパーＧ３送信を行うかどうかの判断用				 */
/* 上記B:3-2は２回線用で回線選択時に使用されているので、意味合いが異なる。		 */
/* 			0:送信時SuperG3で開始する。	1:送信時SuperG3で開始しない				 */
/* by O.Kimoto 1999/04/07														 */
#define SUPER_G3_TX_MASK			0x40
/* --------------------------------------------------------------------------------
B:3-7 ＡＮＳａｍ検出有効／無効の獲得											 */
/* このビットは回線閉結後に何らかのノイズが発生してＶ．８ハンドシェークが中断	 */
/* する場合に、このビットを立てることでＡＮＳａｍを検出しなくなる。				 */
/* ですが、ＤＩＳのビット６（Ｖ．８Ｅｎａｂｌｅ）がセットされていればＣＩ信号を	 */
/* 送出して、再度Ｖ．８に戻ります。												 */
/* 			0:ANSam検出する		1:ANSam検出しない								 */
/* by O.Kimoto 1999/04/07														 */
#define INHIBIT_DETECT_ANSAM		0x80
