	/*------------------------------------------------------------------*/
	SH__ICR |= SH__ICR_DATA;

	/*------------------------------------------------------------------*/
	/* ＤＴＣの割込みをレベルＦへセット                                 */
	/*------------------------------------------------------------------*/
	SH__IPRG |= SH__IPRG_DATA;


	/*------------------------------------------------*/
	/* Ａ／Ｄ変換器の設定                             */
	/*------------------------------------------------*/
	/*----------------------------------------------------------------*/
	/* Ａ／Ｄコントロール／ステータスレジスタ（ＡＤＣＳＲ）の設定     */
