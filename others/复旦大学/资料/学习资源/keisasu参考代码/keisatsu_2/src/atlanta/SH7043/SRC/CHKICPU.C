	AD__ADCSR = AD__ADCSR_DATA;

	/*----------------------------------------------------------------*/
	/* Ａ／Ｄコントロール（ＡＤＣＲ）の設定                           */
	/* ・低消費変換モード                                             */
	/* ・ソフトウェアによるＡ／Ｄ変換の開始を許可                     */
	/* ・スキャンモード                                               */
	/* ・通常サンプリング動作                                         */
	/*----------------------------------------------------------------*/
	AD__ADCR = AD__ADCR_DATA;

	/*----------------------------------------------------------------*/
	/* Ａ／Ｄコントロール／ステータスレジスタ（ＡＤＣＳＲ）の設定     */
	/* ・Ａ／Ｄスタート                                               */
	/*----------------------------------------------------------------*/
	AD__ADCSR |= ADCSR_ADST;


	/*------------------------------------------------*/
	/* データトランスファコントローラ（ＤＴＣ）の設定 */
	/*------------------------------------------------*/
	/*----------------------------------------------------------------*/
	/* ＤＴＣコントロール／ステータスレジスタ（ＤＴＣＳＲ）の設定     */
