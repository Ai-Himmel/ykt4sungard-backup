/* 下記関数はPOPLAR_Hでも同様な仕様変更要求が来ていますので、共通で使用するようにします。
** よって、PRO_COPY_FAX_MODEの条件式の範囲を移動させました。
** by O.Kimoto 1999/08/06
*/
/*************************************************************************
	module		:[コピーモードの画質の初期値獲得]
	function	:[
		1.ユニークＳＷに割り当てられたコピーモードの画質の初期値を返します
	]
	return		:[
		SYS_NORMAL	:標準
		SYS_FINE	:高画質
		SYS_SFINE	:超高画質（スーパーファイン）
		SYS_HFINE	:超高画質（ハイパーファイン）
		SYS_GRAY64	:写真（６４階調）
	]
	common		:[]
	condition	:[]
	comment		:[]
	machine		:[SH7043]
	language	:[SHC]
	keyword		:[CMN]
	date		:[1999/01/21]
	author		:[渡辺一章]
*************************************************************************/
UBYTE CHK_UNI_CopyPrimaryMode(void)
{
	UBYTE	ret;

	switch (SYB_MachineUniqSwitch[UNIQ_SW_J6] & PRIMARY_COPY_MODE_GRAY) {
	case PRIMARY_COPY_MODE_NORMAL:	/* 普通 */
		ret = SYS_NORMAL;
		break;
	case PRIMARY_COPY_MODE_FINE:	/* 高画質 */
		ret = SYS_FINE;
		break;
	case PRIMARY_COPY_MODE_GRAY:	/* 写真 */
		ret = SYS_GRAY64;
		break;
	case PRIMARY_COPY_MODE_SFINE:	/* 超高画質 */
	default:
		if (CHK_UNI_HyperFineON()) {	/* 超高画質がハイパーファインの時 */
			ret = SYS_HFINE;
		}
		else {							/* 超高画質がスーパーファインの時 */
			ret = SYS_SFINE;
		}
		break;
	}

	return(ret);
}
