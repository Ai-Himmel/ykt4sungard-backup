void SetOneshotTimer(UWORD Timer)
{
	/*------------------------------------------------------------------*/
	/* ＴＣＲの設定             										*/
	/* ・ＧＲＡのコンペアマッチ／インプットキャプチャでＴＣＮＴクリア	*/
	/* ・立上がりエッジでカウント			                       		*/
	/* ・内部クロック２８ＭＨｚ÷１６＝５７１．４３ｎｓ					*/
	/* ・予約ビット７は１又は０をセット                                 */
	/*------------------------------------------------------------------*/
	MTU__TCR1 = 0x22;
	/*------------------------------------------------------------------*/
	/* ＴＭＤＲの設定             										*/
	/* ・ＭＴＵチャネル１は通常動作モード                               */
	/* ・予約ビット７は１をセット                                       */
	/*------------------------------------------------------------------*/
	MTU__TMDR1 = 0xC0;
	/*------------------------------------------------------------------*/
	/* ＴＩＯＲＨの設定            										*/
	/* ・ＧＲＢ／ＧＲＡコンペアマッチによる端子出力禁止					*/
	/*------------------------------------------------------------------*/
	MTU__TIOR1 = 0x00;
	/*------------------------------------------------------------------*/
	/* ＴＩＥＲの設定             										*/
	/* ・コンペアマッチ割込み許可（ＴＧＩＡ）							*/
	/* ・予約ビット６は１をセット                                       */
	/* ・予約ビット３，２は０をセット                                   */
	/*------------------------------------------------------------------*/
	MTU__TIER1 = 0x41;
	/*------------------------------------------------------------------*/
	/* ＴＳＲの設定             										*/
	/* ・ＴＣＮＴはダウンカウント                                       */
	/* ・予約ビット６は１をセット                                       */
	/* ・予約ビット３，２は０をセット                                   */
	/*------------------------------------------------------------------*/
	MTU__TSR1 = 0xC0;
	/*------------------------------------------------------------------*/
	/* ＴＧＲの設定             										*/
	/* ・カウンター値の設定				                                */
	/*   Timer * 1750 本ちゃん                                          */
	/*   Timer * 1250 ＩＣＥテスト用                                    */
	/*------------------------------------------------------------------*/
	MTU__TGR1A = (Timer * 1750);
	/*------------------------------------------------------------------*/
	/* ＴＳＴＲの設定             										*/
	/* ・カウンターのスタート				                            */
	/* ・予約ビット５〜３は常に０をセット                               */
	/*------------------------------------------------------------------*/
	MTU__TSTR = (MTU__TSTR + TSTR_START_CH1);
}
