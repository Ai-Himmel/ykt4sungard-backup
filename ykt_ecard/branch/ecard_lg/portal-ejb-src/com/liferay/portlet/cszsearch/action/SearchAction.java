package com.liferay.portlet.cszsearch.action;

import com.liferay.portal.struts.PortletAction;
import com.liferay.util.Validator;

import javax.portlet.PortletConfig;
import javax.portlet.RenderRequest;
import javax.portlet.RenderResponse;

import org.apache.struts.action.ActionForm;
import org.apache.struts.action.ActionForward;
import org.apache.struts.action.ActionMapping;

/**
 * <a href="SearchAction.java.html"><b><i>View Source</i></b></a>
 *
 * @author  Brian Wing Shun Chan
 * @version $Revision: 1.5 $
 *
 */
public class SearchAction extends PortletAction {

	public ActionForward render(
			ActionMapping mapping, ActionForm form, PortletConfig config,
			RenderRequest req, RenderResponse res)
		throws Exception {

		String street = req.getParameter("street");
		String csz = req.getParameter("csz");

		if (Validator.isNotNull(street)) {
			return mapping.findForward(
				"portlet.csz_search.zip4_search_results");
		}
		else if (Validator.isNumber(csz)) {
			return mapping.findForward("portlet.csz_search.zip_search_results");
		}
		else {
			return mapping.findForward(
				"portlet.csz_search.city_state_search_results");
		}
	}

}