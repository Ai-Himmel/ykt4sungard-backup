package com.kingstargroup.fdykt.dto;

public class TPifAreaDTO implements java.io.Serializable {
	
	private String areacode;
	private String areaname;
	private String fareacode;
	private long arealevel;
	private String addr;
	private String remark;
	public String getAreacode() {
		return areacode;
	}
	public void setAreacode(String areacode) {
		this.areacode = areacode;
	}
	public String getAreaname() {
		return areaname;
	}
	public void setAreaname(String areaname) {
		this.areaname = areaname;
	}
	public String getFareacode() {
		return fareacode;
	}
	public void setFareacode(String fareacode) {
		this.fareacode = fareacode;
	}
	public long getArealevel() {
		return arealevel;
	}
	public void setArealevel(long arealevel) {
		this.arealevel = arealevel;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}


    

}
